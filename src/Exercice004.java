
public class Exercice004 {

	public static void main(String[] args) {

		char c = 60, ce = 'e', cg = 'g';
		byte b = 10;

		int a = c + 1;
		System.out.println(a);
		//60(char)+1(int)=61(int)
		int d = 2 * c;
		System.out.println(d);
		//2(int)*60(char)=120(int)
		int e = cg - ce;
		System.out.println(e);
		//g(char)-e(char)=2(int)
		int f = b * c;
		System.out.println(f);
		//10(byte)*60(char)=600(int)
	}

}
